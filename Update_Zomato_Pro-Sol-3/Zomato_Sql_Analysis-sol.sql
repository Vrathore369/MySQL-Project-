-- 11.Calculate the total number of orders placed on weekends.

select 
dayname(order_time) as Day_name,
count(order_id) as total_order from order_detail
where dayofweek(order_time) in (1,7)
group by Day_Name ;

  
-- 12. Calculate the average delivery time (in minutes) for orders placed on weekdays versus weekends.
select case when 
 dayofweek(order_time) in (1,7)then 'weekend'
 else 'weekday' end as day_type,
 count(order_id) as total_orders,
 avg(timestampdiff(minute ,order_time, delivered_time)) as Avg_time
 from order_detail
 group by day_type ;



