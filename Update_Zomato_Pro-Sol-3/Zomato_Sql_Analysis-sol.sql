select 
    food_id,
    food_name,
    price_per_unit as highest_price,
    price_rank
from (
    select 
        food_id,
        food_name,
        price_per_unit,
        dense_rank() over (order by price_per_unit desc) as price_rank
    from foods
) as ranked_foods
where price_rank <= 5
order by highest_price desc;
 
 
-- 14. Find the restaurant with the most diverse menu (i.e., the highest number of food items).
select r.restaurant_id, r.restaurant_name,
 count(distinct (orf.food_id)) as diverse_menu
 from restaurant r
 join order_food orf on r.restaurant_id = orf.restaurant_id
 group by r.restaurant_id, r.restaurant_name
 order by  diverse_menu desc limit 1;
  
-- 15.Calculate total payment amount for each payment type.  
select p.payment_type ,p.payment_status,
count( distinct ofd.order_id) as Total_order,
sum(ofd.quantity * f.price_per_unit) as total_amount
from payment_table p 
join order_food ofd  on p.order_id = ofd.order_id
join foods f on ofd.food_id = f.food_id
group by p.payment_type ,p.payment_status
order by total_amount desc ;
