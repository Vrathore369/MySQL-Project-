 use zomatodb;
 -- 1. List the top 3 customers who have placed the highest number of orders.
 select 
   customer.customer_id ,customer.customer_name, count(order_detail.order_id)  as Highest_No_Of_Order from
   customer
   join order_detail
   on customer.customer_id = order_detail.customer_id
group by  customer.customer_id
order by Highest_No_Of_Order desc limit 3;
   
-- 2.Find the restaurant that has received the highest average rating.

select  
restaurant_id, restaurant_name,avg(rrating) as Rating 
from restaurant
group by restaurant_id ,restaurant_name
 order by  Rating desc limit 1;
 
-- 3.List all orders from order_detail that were delivered in under 30 minutes.
select (order_id),customer_id,
order_time,delivered_time,
timestampdiff(minute, order_time, delivered_time) as delivery_time_in_minutes
from order_detail
where timestampdiff(minute, order_time, delivered_time) < 30;

