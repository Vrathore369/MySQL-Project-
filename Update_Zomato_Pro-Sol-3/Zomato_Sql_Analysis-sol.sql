-- 8. Determine the month with the highest number of total orders placed.
select extract(month from order_time) as month_number,
count(order_id) as total_order 
from order_detail
group by month_number
order by total_order desc limit 1;

-- 9. Calculate the average order amount for each customer. Order by average order amount in descending order.
select customer.customer_id,customer_name ,
avg(order_food.quantity * foods.price_per_unit) as Total_amount
 from customer
 join order_food on customer.customer_id = order_food.customer_id
 join foods  on order_food.food_id = foods.food_id
 group by customer.customer_id,customer_name 
 order by Total_amount desc ;
 
 -- 10.Identify the most frequent customer for each restaurant.
with Customer_Frequency as(
select  customer.customer_id,customer_name,restaurant.restaurant_name,
count(order_detail.restaurant_id )  as Most_Vist_Restaurant,
rank() over ( partition by restaurant.restaurant_name 
order by count(order_detail.restaurant_id ) desc ) as Restaurant_Rank
from customer
join order_detail on customer.customer_id = order_detail.customer_id
join restaurant  on order_detail.restaurant_id  = restaurant.restaurant_id 
group by customer.customer_id,customer_name,restaurant.restaurant_name 

)
select customer_name, restaurant_name  , Most_Vist_Restaurant from Customer_Frequency
  where Restaurant_Rank = 1;
  
  
